import numpy as np

array = np.array([1, 2, 3])

print(array + 5)  # Adding a scalar to each element
print(array - 2)  # Subtracting a scalar from each element
print(array * 3)  # Multiplying each element by a scalar
print(array / 2)  # Dividing each element by a scalar
print(array ** 2) # Squaring each element
print(array % 2)  # Modulus operation on each element
print('--------------------------------')
